# formas de trabalhar com string

# primeira forma com aspas simples
print('vamos cordar todos os dias')

# segunda forma com aspas duplas
print("vamos cordar todos os dias")

# terceira forma quando for ocupar mais de uma linha pode ser usado tripla aspas simples ou tripla aspas duplas
print(''' vamos cordar todos os dias
todos os dias vamos codar''')

# caracteres de escape contra barra N ---> \n
print('olá eu sou uma quebra \n de linha')
# outra forma é ex: quando você quiser colcoar o caminho de uma pasta em um endereço local precisa usasr duas \\
# na frente do que quer que o python ignore
print('arquivo está localizado em \\c:pastas\\arquivo.doc')

