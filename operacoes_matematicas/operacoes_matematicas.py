## operações matemáticas que podem ser feitas no python

# adição
print(10 + 10)

# subtração
print(10 - 5)

# multiplicação
print(10 * 10)


# divisão simples
print(10 / 2)

# divisão de inteiro
print(10 // 6)

# modulus que retona o resto da divisão
print(10 % 6)

# exponencial
print(10 ** 6)

# Atalhos para atribuição de valores
x = 10
x = x + 5
print(x) #
# outra forma mais rapida
y = 10
y += 5
print(y)

